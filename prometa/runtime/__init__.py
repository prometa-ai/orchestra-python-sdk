"""Optional tenant-owned runtime contracts and secure execution kernel.

Install ``prometa-sdk[runtime]`` for cryptographic admission, JSON Schema
enforcement, and the Phase 2A kernel. The default ``prometa-sdk`` installation
remains dependency-free and telemetry-first, and no execution path calls the
Orchestra control plane synchronously. Install
``prometa-sdk[runtime-postgres]`` only for the optional shared durability
adapters, or ``prometa-sdk[runtime-host]`` for the first tenant-deployed
model-only host and its PostgreSQL dependency. Optional bootstrap pull may read
one tenant-selected release handoff, but no tenant request execution path calls
the Orchestra control plane. Install ``prometa-sdk[runtime-mcp]`` on Python
3.10+ for the governed tenant-side MCP transport adapter.
"""

